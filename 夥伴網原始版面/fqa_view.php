<script>
  $(document).ready(function(e) {
    // Scroll View
    $('[data-plugin="scroll-view"]').scrollView({
      threshold: -150,
      onAddClass: function(element) {
        if($(this).is('.fqa-view-list')){
          $('.fqa-view-list').find('li').each(function(i,ele){
            setTimeout(function(){
              $(ele).addClass('shown');
            },i*100);
          });
        }
      }
    });

    // Side Category
    var $cate_btn = $('.side-nav--current');
    var $cate_list = $('.side-nav--list');
    var $cate_second_list = $('.side-nav--list > ul > li > ul');

    $cate_btn.text($cate_list.find('.current').text());
    $cate_list.find('.current').parents('li').addClass('open current');

    $(window).on('resize',function(){
      if( !$cate_btn.is(':visible') && !$cate_list.is(':visible')) 
        $cate_list.show();
      if( $cate_btn.is(':visible') && $cate_list.is(':visible'))
        $cate_list.hide();
    }).trigger('resize');
    
    $(document).on('click', function(event) {
      if (!$(event.target).is('.side-nav--current') && !$(event.target).is('.side-nav--list a') && $cate_btn.is(':visible')){
        if($cate_list.is(':visible')){
          $cate_list.slideUp(300);
        }
      }
    });

    $cate_btn.on('click',function(){
      if(!$cate_list.is(':visible')){
        $cate_list.slideDown(300,function(){
          $(window).trigger('resize.px.parallax');
        });
      }else{
        $cate_list.slideUp(300,function(){
          $(window).trigger('resize.px.parallax');
        });
      }
    });

    $cate_second_list.find('li:has(ul)').addClass('hasul');
    $cate_second_list.find('li').find('ul').hide();
    $cate_second_list.find('li').find('ul:has(li.current)').show();
    $cate_second_list.find('a').on('click',function(event){
      var $this = $(this);
      var $parent = $this.parent('li');
      var $next = $this.next();
      if($next.is('ul')){
        event.preventDefault();
        if(!$next.is(':visible')){
          $next.slideDown(300);
          $parent.siblings('li').find('ul').slideUp(300);
          $parent.siblings('li').removeClass('current');
          if(!$parent.hasClass('current'))
            $parent.addClass('current');
        }else{
          $next.slideUp(300);
        }
      }
    });

    // FQA ACCORDION
    // 錨點Hash在URL最末端才會生效
    var $fqa_list = $('.fqa-view-list');
    var hash = window.location.hash;
    $fqa_list.find('li').filter('.current').removeClass('current');
    $fqa_list.find('li').find('.fqa-a').hide();

    refresh_hash(hash);

    function refresh_hash(_targethash){
      $(_targethash).addClass('current');
      $(_targethash).siblings('li').find('.fqa-a').slideUp(300);
      $(_targethash).find('.fqa-a').slideDown(300,function(){
        $('html, body').animate({
          scrollTop: $(this).parent().offset().top - $('.header').height() - $(_targethash).siblings('li').filter('.current').height()
        },600);
        $(this).parent().siblings('li').filter('.current').removeClass('current');
        $(window).trigger('resize.px.parallax');
      });
    }

    $fqa_list.find('.fqa-q').on('click',function(){
      hash = $(this).attr('href');
      refresh_hash(hash);
    });



  });
</script>
